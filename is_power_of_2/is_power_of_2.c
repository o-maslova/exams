int		is_power_of_2(unsigned int n)
{
	int i;

	i = 0;
	if (n == 0)
		return (0);
	while (n >= 2)
	{
		i = n % 2;
		if (i > 0)
			return (0);
		n = n / 2;
	}
	return (1);
}
